    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
   num2=Double.parseDouble(jTextField1.getText());
    int division012 = 1;		
			switch(operador) {
			case'+':
				result=num1+num2;
				break;
			case'-':
				result=num1-num2;
				break;
			case'*':
				result=num1*num2;
				break;
			case'/':
                            if (num2==0&& operador=='/'){
                          
                               division012=0;
                               break;
                            }else{
				result=num1/num2;
				break;
                            }

			}
                        if (division012==0){jTextField1.setText("No se puede dividir por cero");
                        }else{
                            
                        
			jTextField1.setText(String.valueOf(result));
                        }
                        
                        
    }//GEN-LAST:event_jButton16ActionPerformed

